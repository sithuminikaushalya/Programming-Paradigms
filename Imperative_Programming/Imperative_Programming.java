// Imperative_Programming.java

public class Imperative_Programming {
    public static void main(String[] args) {
        // Initialize variables i and j with the value 10 each
        int j = 10;
        int i = 10;
        
        // Add the values of i and j and store the result in variable k
        int k = i + j;
        
        // Print the value of k
        System.out.println(k);
      
        // Subtract the value of j from the value of i and store the result in variable k
        k = i - j;
        
        // Print the updated value of k
        System.out.println(k);
    }
}
