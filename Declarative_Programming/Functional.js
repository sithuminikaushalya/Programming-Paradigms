// Function to perform calculations
function calculate(i, j) {
    console.log("Sum:", addTwoNumbers(i, j));
    console.log("Difference:", subtractTwoNumbers(i, j));
  }
  
  // Function to add two numbers
  function addTwoNumbers(i, j) {
    return i + j;
  }
  
  // Function to subtract two numbers
  function subtractTwoNumbers(i, j) {
    return i - j;
  }
  
  // Example usage
  calculate(30, 10);